// Function to generate a slug from a given title
export function generateSlug(title: string): string {
  let slug = title.toLowerCase();
  slug = slug.replace(/\W+/g, '-'); // Replaces non-alphanumeric characters with a single dash
  slug = slug.replace(/^-+|-+$/g, ''); // Removes leading and trailing dashes
  return slug;
}




